package com.zhks.safetyproduction.entity;

public class TodoBean {
    private String todoTitle;
    private String todoHandleState;

    public void setTodoTitle(String todoTitle) {
        this.todoTitle = todoTitle;
    }

    public void setTodoHandleState(String todoHandleState) {
        this.todoHandleState = todoHandleState;
    }

    public String getTodoTitle() {
        return todoTitle;
    }

    public String getTodoHandleState() {
        return todoHandleState;
    }
}
